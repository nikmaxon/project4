def upper_text(text):
    """
     Функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами.
    """
    return str.upper(text)


def title_text(text):
    """
    Вторую функцию, которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции
    """
    return str.title(text)
